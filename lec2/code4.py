# Arithmetic
num1 = 1
num2 = 3 + 4j
print(num1 - num2)

# Multiplication(*)
num3 = 1 + 2j
num4 = 2 + 3j
print(num3 * num4)

# Division(/)
num5 = 4 + 2j
num6 = 1 + 1j
print(num5 / num6) # Always output float value

# Floor Division(//)
num7 = 9.5
num8 = 2.0
print(num7 // num8)

# Modulus(%)
num9 = 9.3
num10 = 2.1
print(num9 % num10)

# Exponentiation(**)
num11 = 1 + 1j
num12 = 2
print(num11 ** num12)

# Comparison Operators
a = 5
b = 5
print(a <= b)

# Bitwise Operators
a = 6 # 110
b = 3 # 011
print(a ^ b)

# Logical Operators
a = 6 # 110
b = 3 # 011
print(not(a < 5))